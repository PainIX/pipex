#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*p_1;
	t_list	*p_2;

	p_1 = lst;
	p_2 = lst;
	while (p_1 != NULL)
	{
		p_2 = p_1;
		p_1 = p_1->next;
	}
	return (p_2);
}
